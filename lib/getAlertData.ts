import yaml from "js-yaml";

/**
 * @deprecated Just use the JSON instead.
 * @returns 
 */
export async function getAlertData(): Promise<RulesJson> {

    const response = await fetch(
        "https://raw.githubusercontent.com/samber/awesome-prometheus-alerts/refs/heads/master/_data/rules.yml",
        {},
    );
    
    if (!response.ok) {
        throw Error("All is not daijobu.");
    }
    
    const rawYml = await (await response.blob()).text();
    
    const json = yaml.load(rawYml) as RulesJson;
    if (!json) {
        throw Error("JSON generated by js-yaml was null.");
    }
    return json;
}

// Approximations for the type.
type Rule = {
	name: string;
	description: string;
	query: string;
	severity: string;
	for: string;
	comments: string;
	summary: string;
};
type Exporter = {
	slug: string;
	rules?: Rule[];
	name: string;
	doc_url: string;
};
type Service = {
	name: string;
	exporters: Exporter[];
};
type Group = {
	name: string;
	services: Service[];
};
type RulesJson = {
	groups: Group[];
};